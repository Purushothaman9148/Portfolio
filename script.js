function MenuHomeClick() {
    let menuhomeid = document.getElementById('home');
    let menuaboutid = document.getElementById('about');
    let menuskillsid = document.getElementById('skills');
    let menuprojectid = document.getElementById('project');
    let menucontactid = document.getElementById('contact');
    menuhomeid.setAttribute("class", "nav-link active");
    menuaboutid.setAttribute("class", "nav-link");
    menuskillsid.setAttribute("class", "nav-link");
    menuprojectid.setAttribute("class", "nav-link");
    menucontactid.setAttribute("class", "nav-link");
}

